/*
Operadores de comparação
> maior que
< menor que
>= maior ou igual a
<= menor ou igual a
== igualdade (valor)
=== igualdade extrita
!= diferente
!== diferente extrito
*/

console.log(5 > 3); //true
console.log(5 < 3); //false
console.log(5 >= 5); //true
console.log(5 <= 5); //true
console.log(5 == 5); //true

const comp = 10 > 5;
console.log(comp); //true

const comp2 = 10 < 5;
console.log(comp2); //false

const comp3 = 10 <= 5;
console.log(comp3); //false

const comp4 = 10 >= 5;
console.log(comp4); //true
