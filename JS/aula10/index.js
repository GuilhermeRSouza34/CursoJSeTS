/*
Operadores de comparação
> maior que
< menor que
>= maior ou igual a
<= menor ou igual a
== igualdade (valor)
=== igualdade extrita
!= diferente
!== diferente extrito
*/

console.log(5 > 3); //true
console.log(5 < 3); //false
console.log(5 >= 5); //true
console.log(5 <= 5); //true
console.log(5 == 5); //true